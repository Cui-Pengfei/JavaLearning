package cpf.learn.template;

public class Template {
    //   main + enter 直接出现main函数
    public static void main(String[] args) {
        // sout + enter 直接出现输出语句
        System.out.println("我是谁？");

        //  fori + enter 直接出现循环体
        for (int index = 0; index < 10; index++) {
            System.out.println("我是你爸爸！");
        }






    }//end main
}//end class
